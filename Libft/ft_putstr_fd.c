#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	cnt;

	cnt = 0;
	while (s[cnt] != '\0')
	{
		ft_putchar_fd(s[cnt], fd);
		cnt++;
	}
	return ;
}
